package net.pedroksl.ae2addonlib.client.widgets;

/**
 * This enum contains the lib's provided {@link IActionEnum}.
 * These entries are used in the implementation of the lib's own Menus.
 */
public enum AddonActionItems implements IActionEnum {
    /**
     * Action item used to create the Fluid Flush ActionButton appearance
     */
    FLUID_FLUSH,
    /**
     * Action item used to create the Clear ActionButton appearance
     */
    CLEAR,
    /**
     * Action item used to create the Directional Output ActionButton appearance
     */
    DIRECTIONAL_OUTPUT;
}
